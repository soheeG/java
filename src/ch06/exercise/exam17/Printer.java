package ch06.exercise.exam17;

public class Printer {
	
	public static void println(int param1) {
		System.out.println(param1);
	}
	
	public static void println(boolean param1) {
		System.out.println(param1);
	}
	
	public static void println(double param1) {
		System.out.println(param1);
	}
	
	public static void println(String param1) {
		System.out.println(param1);
	}

}
