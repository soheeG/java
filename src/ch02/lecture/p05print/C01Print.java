package ch02.lecture.p05print;

public class C01Print {
	public static void main(String[] args) {
		// println(parameter)
		// 파라미터 출력 후 엔터 출력
		System.out.println("hello");
		System.out.println("world");
		System.out.println(true);
		
		// println()
		// 파라미터 없으면 엔터만 출력
		System.out.println();
		System.out.println();
		
		System.out.println("새줄 출력됨??");
	}
}
