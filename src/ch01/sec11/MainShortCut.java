package ch01.sec11;

public class MainShortCut {
	public static void main(String[] args) { 
		// main + ctrl + space : main 메소드 작성
		System.out.println("Hi JAVA");
		System.out.println("출력 되는 내용"); 
		// sysout + ctrl + space : System.out.println() 작성
		// ctrl + / : 주석
		// ctrl + m : 창크기 변경
		// ctrl + d : 한 줄 지우기
		// alt + 방향키 : 위 아래 이동
		// ctrl + space : 힌트 얻기
	}
}
